def get_texts(language):
    return TRANSLATIONS.get(language, TRANSLATIONS[DEFAULT_LANGUAGE])


class PDFFlipbookGeneratorApp:
    def __init__(self, root):
        self.root = root
        self.root.title(APP_NAME)
        self.root.geometry("450x285")
        self.root.configure(padx=20, pady=20)
        self.root.resizable(False, False)
        
        self.pdf_path = None
        self.is_running = False
        self.language_var = tk.StringVar(value=DEFAULT_LANGUAGE)
        self.language_options = {
            texts["language_name"]: code
            for code, texts in TRANSLATIONS.items()
        }
        
        # UI elements
        self.lbl_title = tk.Label(root, text=APP_NAME, font=("Segoe UI", 14, "bold"))
        self.lbl_title.pack(pady=(0, 12))

        frame_language = tk.Frame(root)
        frame_language.pack(pady=(0, 12))

        self.lbl_language = tk.Label(frame_language)
        self.lbl_language.grid(row=0, column=0, padx=(0, 8))

        self.cmb_language = ttk.Combobox(
            frame_language,
            state="readonly",
            width=15,
            values=list(self.language_options.keys()),
        )
        self.cmb_language.grid(row=0, column=1)
        self.cmb_language.set(TRANSLATIONS[DEFAULT_LANGUAGE]["language_name"])
        self.cmb_language.bind("<<ComboboxSelected>>", self.change_language)
        
        self.lbl_file = tk.Label(root, fg="gray")
        self.lbl_file.pack(pady=(0, 10))
        
        frame_btns = tk.Frame(root)
        frame_btns.pack(pady=10)
        
        self.btn_select = tk.Button(frame_btns, command=self.select_file, width=15)
        self.btn_select.grid(row=0, column=0, padx=5)
        
        self.btn_start = tk.Button(frame_btns, command=self.start_conversion, width=15, state=tk.DISABLED)
        self.btn_start.grid(row=0, column=1, padx=5)
        
        self.lbl_status = tk.Label(root, text="")
        self.lbl_status.pack(pady=(10, 5))
        
        self.progress = ttk.Progressbar(root, orient="horizontal", length=400, mode="determinate")
        self.progress.pack()
        self.update_ui_texts()

    def texts(self):
        return get_texts(self.language_var.get())

    def change_language(self, _event=None):
        selected_name = self.cmb_language.get()
        self.language_var.set(self.language_options.get(selected_name, DEFAULT_LANGUAGE))
        self.update_ui_texts()

    def update_ui_texts(self):
        texts = self.texts()
        self.lbl_language.config(text=texts["language_label"])
        self.btn_select.config(text=texts["select_file"])
        self.btn_start.config(text=texts["start"])
        if self.pdf_path:
            short_name = os.path.basename(self.pdf_path)
            if len(short_name) > 40:
                short_name = short_name[:37] + "..."
            self.lbl_file.config(text=texts["selected_file"].format(file_name=short_name), fg="black")
        else:
            self.lbl_file.config(text=texts["no_pdf_selected"], fg="gray")

    def select_file(self):
        texts = self.texts()
        file_path = filedialog.askopenfilename(
            title=texts["file_dialog_title"],
            filetypes=[(texts["file_type_pdf"], "*.pdf")]
        )
        if file_path:
            self.pdf_path = file_path
            short_name = os.path.basename(file_path)
            if len(short_name) > 40:
                short_name = short_name[:37] + "..."
            self.lbl_file.config(text=texts["selected_file"].format(file_name=short_name), fg="black")
            self.btn_start.config(state=tk.NORMAL)

    def start_conversion(self):
        if not self.pdf_path: return
        self.is_running = True
        self.active_language = self.language_var.get()
        self.btn_select.config(state=tk.DISABLED)
        self.btn_start.config(state=tk.DISABLED)
        self.cmb_language.config(state=tk.DISABLED)
        self.progress['value'] = 0
        self.lbl_status.config(text=get_texts(self.active_language)["starting"])
        
        # Run in the background to keep the UI responsive.
        threading.Thread(target=self.process_pdf, daemon=True).start()

    def update_progress(self, val, text):
        self.progress['value'] = val
        self.lbl_status.config(text=text)

    def process_pdf(self):
        language = getattr(self, "active_language", DEFAULT_LANGUAGE)
        texts = get_texts(language)
        try:
            base_name = os.path.splitext(os.path.basename(self.pdf_path))[0]
            output_dir = os.path.join(os.path.dirname(self.pdf_path), f"{base_name}_flipbook")
            
            os.makedirs(output_dir, exist_ok=True)
            clean_generated_outputs(output_dir)
            self.root.after(0, self.update_progress, 0, texts["loading_pdf"])
            
            doc = fitz.open(self.pdf_path)
            try:
                page_count = len(doc)
                
                self.root.after(0, self.update_progress, 5, texts["pages_found"].format(page_count=page_count))
                
                page0 = doc.load_page(0)
                w = page0.rect.width
                h = page0.rect.height
                # A single-page PDF should keep the original page aspect ratio.
                is_single_page = page_count == 1
                calculated_aspect_ratio = (w / h) if is_single_page else (w * 2) / h
                # High-resolution rendering for sharp page images.
                zoom = RENDER_ZOOM
                mat = fitz.Matrix(zoom, zoom)
                images = []
                
                for i in range(page_count):
                    page = doc.load_page(i)
                    pix = page.get_pixmap(matrix=mat)
                    
                    img_name = f"page_{i+1}.jpg"
                    img_path = os.path.join(output_dir, img_name)
                    pix.save(img_path, jpg_quality=JPEG_QUALITY)
                    pix = None
                    images.append(img_name)
                    
                    # Keep conversion progress within the 5%-95% range.
                    progress_val = 5 + (90 * (i + 1) / page_count)
                    self.root.after(
                        0,
                        self.update_progress,
                        progress_val,
                        texts["converting_page"].format(current=i + 1, total=page_count),
                    )
            finally:
                doc.close()

            self.root.after(0, self.update_progress, 98, texts["creating_files"])
            create_static_assets(output_dir, images, base_name, calculated_aspect_ratio, page_count, language)
            
            self.root.after(0, self.update_progress, 100, texts["done"])
            self.root.after(0, self.finish_success, output_dir)
            
        except Exception as e:
            self.root.after(0, self.show_error, str(e))
            self.root.after(0, self.reset_ui, texts["generic_error"])

    def reset_ui(self, msg):
        self.is_running = False
        self.lbl_status.config(text=msg)
        self.btn_select.config(state=tk.NORMAL)
        self.btn_start.config(state=tk.NORMAL if self.pdf_path else tk.DISABLED)
        self.cmb_language.config(state="readonly")

    def finish_success(self, output_dir):
        texts = get_texts(getattr(self, "active_language", self.language_var.get()))
        self.reset_ui(texts["conversion_complete"].format(folder_name=os.path.basename(output_dir)))
        messagebox.showinfo(texts["success_title"], texts["success_message"])
        if os.name == 'nt':
            os.startfile(output_dir)

    def show_error(self, err_msg):
        texts = get_texts(getattr(self, "active_language", self.language_var.get()))
        messagebox.showerror(texts["error_title"], texts["conversion_error"].format(error=err_msg))

