def is_generated_page_image(file_name):
    stem, ext = os.path.splitext(file_name)
    return ext.lower() == ".jpg" and stem.startswith("page_") and stem[5:].isdigit()


def clean_generated_outputs(output_dir):
    if not os.path.isdir(output_dir):
        return

    for entry_name in os.listdir(output_dir):
        entry_path = os.path.join(output_dir, entry_name)
        if not os.path.isfile(entry_path):
            continue
        if entry_name in GENERATED_STATIC_FILES or is_generated_page_image(entry_name):
            os.remove(entry_path)


